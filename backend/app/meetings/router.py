@router.post(
    "/{meeting_id}/guests", response_model=MeetingGuestOut, status_code=status.HTTP_201_CREATED
)
async def add_meeting_guest(
    meeting_id: UUID,
    payload: MeetingGuestCreate,
    current_user: User = Depends(get_current_user),
    session: AsyncSession = Depends(get_session),
):
    stmt = select(Meeting).where(Meeting.id == meeting_id)
    res = await session.execute(stmt)
    meeting = res.scalar_one_or_none()

    if not meeting:
        raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Meeting not found.")

    await check_meeting_write_permission(session, current_user, meeting)

    guest = MeetingGuest(
        meeting_id=meeting.id,
        name=payload.name,
        phone=payload.phone,
        email=payload.email,
        office=payload.office,
        department=payload.department,
        designation=payload.designation,
    )
    session.add(guest)
    await session.flush()

    await log_audit_event(
        session, current_user.id, "MEETING_GUEST_ADDED", "meeting_guest", guest.id
    )
    await session.commit()

    # Reload guest to populate autogenerated fields post-commit
    stmt = select(MeetingGuest).where(MeetingGuest.id == guest.id)
    guest = (await session.execute(stmt)).scalar_one()

    return MeetingGuestOut.model_validate(guest)


@router.delete("/{meeting_id}/guests/{guest_id}", status_code=status.HTTP_204_NO_CONTENT)
async def remove_meeting_guest(
    meeting_id: UUID,
    guest_id: UUID,
    current_user: User = Depends(get_current_user),
    session: AsyncSession = Depends(get_session),
):
    stmt = select(Meeting).where(Meeting.id == meeting_id)
    res = await session.execute(stmt)
    meeting = res.scalar_one_or_none()

    if not meeting:
        raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Meeting not found.")

    await check_meeting_write_permission(session, current_user, meeting)

    guest_stmt = select(MeetingGuest).where(
        and_(MeetingGuest.id == guest_id, MeetingGuest.meeting_id == meeting_id)
    )
    guest = (await session.execute(guest_stmt)).scalar_one_or_none()

    if not guest:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND, detail="Guest not found in this meeting."
        )

    await session.delete(guest)
    await log_audit_event(
        session, current_user.id, "MEETING_GUEST_REMOVED", "meeting_guest", guest_id
    )
    await session.commit()
